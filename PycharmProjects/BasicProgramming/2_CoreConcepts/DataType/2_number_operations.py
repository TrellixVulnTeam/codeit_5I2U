#1.버림나눗셈 - 나눈값의 소수를 버려 10^0자리 이상만을 보여줌
print(7//2) #3
print(8//3) #2
print(8.0//3) #2 피연산자 둘중하나 소수일경우 결과값도 소수

#2. round(반올림) 함수 - 소수점 밑의 수 반올림한값 반환
print(round(3.1415926535)) #3
print(round(3.1415926535, 4)) #3.1416 소수점 아래 4자리까지 표시 5쨰자리수에서 반올림